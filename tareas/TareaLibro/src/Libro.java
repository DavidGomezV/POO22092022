public class Libro {

int NumPags;
String Titulo;
String autor;
String editorial;
int releaseDate;  

    public Libro() {
    }

    public Libro(int NumPags, String Titulo, String autor, String editorial, int releaseDate) {
        this.NumPags = NumPags;
        this.Titulo = Titulo;
        this.autor = autor;
        this.editorial = editorial;
        this.releaseDate = releaseDate;
    }


}
